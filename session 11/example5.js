let word = "apple";
// do - while loop
let n = 0; // initialisation statement 
do {
    console.log(word);

    n++; // increment or decrement
}while( n < 3 ); //condition

// n=0, n=1, n=2, n=3 
console.log("\nAfter do-while loop.");

